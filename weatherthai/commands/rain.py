import typer
from weatherthai.api_client import get_forecast

RAIN_KEYWORDS = ["rain", "shower", "thunderstorm", "drizzle"]

def rain_command(
    city: str = typer.Option(..., help="‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏°‡∏∑‡∏≠‡∏á‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏î‡∏π‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏™‡∏µ‡πà‡∏¢‡∏á‡∏ù‡∏ô‡∏ï‡∏Å"),
    hours: int = typer.Option(6, help="‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á‡∏ñ‡∏±‡∏î‡πÑ‡∏õ (3‚Äì24)")
):
    """‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏™‡∏µ‡πà‡∏¢‡∏á‡∏ù‡∏ô‡∏ï‡∏Å‡∏£‡∏≤‡∏¢‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á (‡∏õ‡∏£‡∏∞‡∏°‡∏≤‡∏ì‡∏Å‡∏≤‡∏£‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• 3 ‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á)"""

    if hours < 3 or hours > 24:
        typer.echo("‚ùå ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á‡∏ï‡πâ‡∏≠‡∏á‡∏≠‡∏¢‡∏π‡πà‡∏£‡∏∞‡∏´‡∏ß‡πà‡∏≤‡∏á 3‚Äì24")
        return

    try:
        data = get_forecast(city)
    except Exception as e:
        typer.echo(f"‚ùå ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß: {e}")
        return

    typer.echo(f"\nüåß ‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏™‡∏µ‡πà‡∏¢‡∏á‡∏ù‡∏ô‡∏ï‡∏Å‡∏£‡∏≤‡∏¢‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö {city}\n")

    # forecast data -> list every 3 hours
    forecasts = data.get("list", [])[: hours // 3]

    for item in forecasts:
        time_txt = item["dt_txt"]
        weather_desc = item["weather"][0]["description"]
        pop = item.get("pop", 0) * 100  # probability of precipitation

        # ‡∏õ‡∏£‡∏∞‡πÄ‡∏°‡∏¥‡∏ô‡∏à‡∏≤‡∏Å keyword
        risk = "‡∏°‡∏µ‡πÇ‡∏≠‡∏Å‡∏≤‡∏™‡∏™‡∏π‡∏á" if any(k in weather_desc.lower() for k in RAIN_KEYWORDS) else "‡∏ï‡πà‡∏≥"

        typer.echo(
            f"- {time_txt}: "
            f"{weather_desc} | POP: {pop:.0f}% | ‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏™‡∏µ‡πà‡∏¢‡∏á: {risk}"
        )
